package org.loggers.beans;

public class StackoverflowConfiguration {
    private String key;
    private String site;

    public StackoverflowConfiguration() {
        // Default Constructor
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }
}
